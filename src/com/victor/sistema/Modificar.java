    private void ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ActualizarActionPerformed
        DefaultTableModel modelotabla = new DefaultTableModel();
        tabla.setModel(modelotabla);

        try {
            conexion=getConnection( );

            ps= conexion.prepareStatement("select * from clientes");
             
            rs= ps.executeQuery();

            modelotabla.addColumn("Dni");
            modelotabla.addColumn("Nombre");
            modelotabla.addColumn("Apellidos");
            modelotabla.addColumn("Fecha");
            modelotabla.addColumn("Sexo");
            modelotabla.addColumn("Gmail");

            while(rs.next()) {
                Object fila[] =new Object[6];
                for( int i=0; i<6; i++){
                    fila[i]= rs.getObject(i+1);
                }
                modelotabla.addRow(fila);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,ex);
        }

    }//GEN-LAST:event_ActualizarActionPerformed

    private void bBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bBuscarActionPerformed
        
        
        try {
            conexion=getConnection();
            ps=conexion.prepareStatement("select * from clientes where Dni='"+lDNI.getText()+"'");
            
            rs=ps.executeQuery();
                lDNI.setText(""); 
                lNombre.setText(""); 
                lApellidos.setText(""); 
                bHombre.setSelected(false);
                bMujer.setSelected(false);
                lgmail.setText("");
                
            if(rs.next()){
                lDNI.setText(rs.getString("Dni"));
                lNombre.setText(rs.getString("Nombre"));
                lApellidos.setText(rs.getString("Apellidos"));
                fecha.setDate(rs.getDate("fecha"));
                lgmail.setText(rs.getString("Gmail"));
                String a=rs.getString("Sexo");
                  
                  if(a.equals("Masculino")){
                 
                    bHombre.setSelected(true);
                   
                  }if(a.equals("Femenino")){
 
                  bMujer.setSelected(true);
                  } 
                  
            }else{
                JOptionPane.showMessageDialog(null,"Persona no encontrada");
            }
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,ex);
        }
        
    }//GEN-LAST:event_bBuscarActionPerformed

