    private void bModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bModificarActionPerformed
        int anho=fecha.getCalendar().get(Calendar.YEAR);
        int mes=fecha.getCalendar().get(Calendar.MONTH);
        int dia=fecha.getCalendar().get(Calendar.DAY_OF_MONTH);

        Date fecha=Date.valueOf(anho+"-"+mes+"-"+dia);

        String sexo="G";

        if(bHombre.isSelected()){
            sexo="Masculino";
        }if(bMujer.isSelected()){
            sexo="Femenino";
        }

        try {
            conexion=getConnection( );
            ps= conexion.prepareStatement("UPDATE clientes SET Dni ='"+lDNI.getText()+"',Nombre ='"+lNombre.getText()+"',Apellidos ='"+lApellidos.getText()+"',Fecha ='"+fecha+"',Sexo ='"+sexo+"',Gmail ='"+lgmail.getText()+"' where Dni ='"+lDNI.getText()+"'");
            
            int resultado= ps.executeUpdate();

            if(resultado>0){
                JOptionPane.showMessageDialog(null,"Modificado correctamente");
            }else{
                JOptionPane.showMessageDialog(null,"Error al modificar");
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,ex);
        }

    }//GEN-LAST:event_bModificarActionPerformed

